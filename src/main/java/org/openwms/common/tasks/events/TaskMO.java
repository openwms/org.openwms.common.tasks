package org.openwms.common.tasks.events;

import com.fasterxml.jackson.annotation.JsonFormat;
import org.openwms.common.tasks.api.TaskState;

import java.io.Serializable;
import java.time.ZonedDateTime;

import static org.openwms.common.tasks.TimeProvider.DATE_TIME_WITH_TIMEZONE;

/**
 * A TaskMO.
 *
 * @author Heiko Scherrer
 */
public class TaskMO implements Serializable {

    private String pKey;
    private String taskId;
    private String description;
    private String type;
    private TaskState state;
    @JsonFormat(pattern = DATE_TIME_WITH_TIMEZONE)
    private ZonedDateTime startedAt;
    @JsonFormat(pattern = DATE_TIME_WITH_TIMEZONE)
    private ZonedDateTime finishedAt;

    public String getpKey() {
        return pKey;
    }

    public void setpKey(String pKey) {
        this.pKey = pKey;
    }

    public String getTaskId() {
        return taskId;
    }

    public void setTaskId(String taskId) {
        this.taskId = taskId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public TaskState getState() {
        return state;
    }

    public void setState(TaskState state) {
        this.state = state;
    }

    public ZonedDateTime getStartedAt() {
        return startedAt;
    }

    public void setStartedAt(ZonedDateTime startedAt) {
        this.startedAt = startedAt;
    }

    public ZonedDateTime getFinishedAt() {
        return finishedAt;
    }

    public void setFinishedAt(ZonedDateTime finishedAt) {
        this.finishedAt = finishedAt;
    }
}
